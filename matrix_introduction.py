# написать матрицу с ее введением (массив с вложенным массивом [[], [], []])

# [1, 2, 3]
# [2, 3, 4]
# [5, 6, 7]

# [[1, 2, 3], [2, 3, 4], [5, 6, 7]]

t = [[1, 2, 3], [2, 3, 4], [5, 6, 7]]
for both in t:
    a, b, c = both
    print(a, b, c)

